public class Point {
    int x,y,pos;
    public Point(int x,int y,int pos)
    {
        this.x=x;
        this.y=y;
        this.pos=pos;
    }

    public int getx()
    {
        return x;
    }
    public int gety()
    {
        return y;
    }
    public int getpos()
    {
        return pos;
    }
}